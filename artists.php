     <figure>
         <img src="../final/Kristel.jpg" alt="" class="artists">
              <figcaption>Jennie Kristel studied art in Massachusetts when she was just in high school and went on to get her MA in Expressive Therapies at Lesley University. She now lives in Burlington, where is an expressive therapist. She uses art to work as a teacher and healer. Her art has been featured at Vermont Studio Center as well as exhibits throughout the Northeast.
<br>
<br>
Kristel creates 2D art, such as paintings and prints, along with functional objects, such as lampshades, journals, and handmade envelopes. She also uses art to express the connection between human experience and the natural landscape. Her work focuses on spirituality and emotion and uses objects such as feathers and leaves. 
